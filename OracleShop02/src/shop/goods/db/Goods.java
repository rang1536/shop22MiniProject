package shop.goods.db;

public class Goods {

	private int goodsNum;
	private String goodsMemberId;
	private String goodsName;
	private int goodsCate;
	private int goodsPrice;
	private int goodsColor;
	private int goodsSize;
	private String goodsDate;
	private String goodsImg;
	private String goodsDetail;
	private int goodsAmount;
	public int getGoodsNum() {
		return goodsNum;
	}
	public void setGoodsNum(int goodsNum) {
		this.goodsNum = goodsNum;
	}
	public String getGoodsMemberId() {
		return goodsMemberId;
	}
	public void setGoodsMemberId(String goodsMemberId) {
		this.goodsMemberId = goodsMemberId;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public int getGoodsCate() {
		return goodsCate;
	}
	public void setGoodsCate(int goodsCate) {
		this.goodsCate = goodsCate;
	}
	public int getGoodsPrice() {
		return goodsPrice;
	}
	public void setGoodsPrice(int goodsPrice) {
		this.goodsPrice = goodsPrice;
	}
	public int getGoodsColor() {
		return goodsColor;
	}
	public void setGoodsColor(int goodsColor) {
		this.goodsColor = goodsColor;
	}
	public int getGoodsSize() {
		return goodsSize;
	}
	public void setGoodsSize(int goodsSize) {
		this.goodsSize = goodsSize;
	}
	public String getGoodsDate() {
		return goodsDate;
	}
	public void setGoodsDate(String goodsDate) {
		this.goodsDate = goodsDate;
	}
	public String getGoodsImg() {
		return goodsImg;
	}
	public void setGoodsImg(String goodsImg) {
		this.goodsImg = goodsImg;
	}
	public String getGoodsDetail() {
		return goodsDetail;
	}
	public void setGoodsDetail(String goodsDetail) {
		this.goodsDetail = goodsDetail;
	}
	public int getGoodsAmount() {
		return goodsAmount;
	}
	public void setGoodsAmount(int goodsAmount) {
		this.goodsAmount = goodsAmount;
	}
	
	
	
}
